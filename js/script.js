const container = document.getElementById('icons-container');

function printIcons(filteredIcons){
	container.innerHTML = '';
	filteredIcons.forEach((icon) => {
		const card = document.createElement('div');
		card.setAttribute('class','card');
		card.innerHTML=`
		<i class= "fas fa-${icon.name}" style="color:${icon.color}"></i>
		<div class="title">${icon.name.toUpperCase()}</div>
		`
		container.append(card);
	})
}

printIcons(icons);

const typeElement = document.getElementById('type-filter');
typeElement.addEventListener('change', function(){
	const iconType = this.value;
	const filteredIcons = icons.filter((icon)=>{
		return icon.type === iconType || !iconType
	})
	printIcons(filteredIcons)

})


